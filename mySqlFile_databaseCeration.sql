use medical;
CREATE TABLE healthcare (
    Name VARCHAR(100),
    Age INT,
    Gender VARCHAR(10),
    Blood_Type VARCHAR(5),
    Medical_Condition VARCHAR(50),
    Date_of_Admission DATE,
    Discharge_Date DATE,
    Admission_Type VARCHAR(20),
    Room_Number INT,
    Doctor VARCHAR(50),
    Hospital VARCHAR(50),
    Insurance_Provider VARCHAR(50),
    Billing_Amount DECIMAL(10,2),
    Test_Results VARCHAR(50),
    Height DECIMAL(5,2),
    Weight DECIMAL(5,2),
    BMI DECIMAL(5,2),
    BMI_Category VARCHAR(20),
    Systolic_BP INT,
    Diastolic_BP INT,
    Cholesterol VARCHAR(20),
    Glucose VARCHAR(20),
    Smoke VARCHAR(5),
    Alcohol_Intake VARCHAR(5),
    Physically_Active VARCHAR(5),
    Lifestyle_Index INT,
    Heart_Disease_Risk VARCHAR(5)
);
